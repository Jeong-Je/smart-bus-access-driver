/*-------------------------------------------------Global variables--------------------------------------------------*/
extern StopBtnState g_stopBtnState;
extern DisabledStopBtnState g_disabledStopBtnState;

extern bool buzzerOn;
extern uint32 buzzerStart;

extern uint8 monitorFlags;


/*********************************************************************************************************************/

/*********************************************************************************************************************/
/*--------------------------------------------Private Variables/Constants--------------------------------------------*/
/*********************************************************************************************************************/

