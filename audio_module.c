#define ASC_TX_BUF_SIZE (64)
#define ASC_RX_BUF_SIZE (64)

#define AUDIO_MODULE_BAUD_RATE  (9600)
#define AUDIO_MODULE_MAX_VOLUME (30U)

#define INTPRIO_ASCLIN_TX  (1)
#define INTPRIO_ASCLIN_RX  (2)

IFX_INTERRUPT(asclinTxISR, 0, INTPRIO_ASCLIN_TX);
IFX_INTERRUPT(asclinRxISR, 0, INTPRIO_ASCLIN_RX);

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/

uint8 ascTxBuf[ASC_TX_BUF_SIZE + sizeof(Ifx_Fifo) + 8];
uint8 ascRxBuf[ASC_RX_BUF_SIZE + sizeof(Ifx_Fifo) + 8];

IfxAsclin_Asc audioModuleHandle;

/*********************************************************************************************************************/
/*--------------------------------------------Private Variables/Constants--------------------------------------------*/
/*********************************************************************************************************************/



/*********************************************************************************************************************/
