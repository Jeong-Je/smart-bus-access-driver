/*********************************************************************************************************************/

/* UART 통신용 ISR */
void asclinTxISR(void);
void asclinRxISR(void);

void initAudioModule(IfxAsclin_Tx_Out* pTxPort, IfxAsclin_Rx_In* pRxPort, void* pAsclinModule);
void resetAudioModule(void);

void sendAudioModulePacket(uint8 cmd, uint8 paramHigh, uint8 paramLow);

void setAudioModuleVolume(uint8 volume);
void playTrack(uint8 track);

/* 오디오 모듈 알림 재생 함수들 */
void playDoorOpenSound(void);
void playDoorCloseSound(void);
void playSlopeOpenSound(void);
void playSlopeCloseSound(void);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/

void asclinTxISR(void)
{
    IfxAsclin_Asc_isrTransmit(&audioModuleHandle);
}

void asclinRxISR(void)
{
    IfxAsclin_Asc_isrReceive(&audioModuleHandle);
}

void initAudioModule(IfxAsclin_Tx_Out* pTxPort, IfxAsclin_Rx_In* pRxPort, void* pAsclinModule)
{
    IfxAsclin_Asc_Config ascConfig;

    IfxAsclin_Asc_initModuleConfig(&ascConfig, pAsclinModule);

    //ascConfig.clockSource = IfxAsclin_ClockSource_kernelClock;
    ascConfig.baudrate.baudrate = AUDIO_MODULE_BAUD_RATE;
    ascConfig.interrupt.txPriority = INTPRIO_ASCLIN_TX;
    ascConfig.interrupt.rxPriority = INTPRIO_ASCLIN_RX;
    ascConfig.interrupt.typeOfService = 0;

    // 핀 설정
    const IfxAsclin_Asc_Pins pins = {
            NULL_PTR,   IfxPort_InputMode_pullUp,
            pRxPort,    IfxPort_InputMode_pullUp,
            pTxPort,    IfxPort_OutputMode_pushPull,
            NULL_PTR,   IfxPort_OutputMode_pushPull,
            IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    ascConfig.pins = &pins;

    // 버퍼 설정
    ascConfig.txBuffer = ascTxBuf;
    ascConfig.txBufferSize = ASC_TX_BUF_SIZE;
    ascConfig.rxBuffer = ascRxBuf;
    ascConfig.rxBufferSize = ASC_RX_BUF_SIZE;

    // 모듈 초기화
    IfxAsclin_Asc_initModule(&audioModuleHandle, &ascConfig);
}

void sendAudioModulePacket(uint8 cmd, uint8 paramHigh, uint8 paramLow)
{
    uint8 packet[10] = { 0x7E, 0xFF, 0x06, cmd, 0x00, paramHigh, paramLow, 0x00, 0x00, 0xEF };

    uint16 checksum = 0xFFFF - (packet[1] + packet[2] + packet[3] + packet[4] + packet[5] + packet[6]) + 0x1;
    packet[7] = (uint8)(checksum >> 8);
    packet[8] = (uint8)(checksum & 0x00FF);

    // Asc드라이버 전송 로직
    Ifx_SizeT count = 10;
    IfxAsclin_Asc_write(&audioModuleHandle, packet, &count, TIME_INFINITE);
}

void resetAudioModule(void)
{
    sendAudioModulePacket(0x0C, 0x00, 0x00);
}

void setAudioModuleVolume(uint8 volume)
{
    uint8 value = (volume < AUDIO_MODULE_MAX_VOLUME) ? volume : AUDIO_MODULE_MAX_VOLUME;
    sendAudioModulePacket(0x06, 0x00, value);
}

void playTrack(uint8 track)
{
    sendAudioModulePacket(0x0F, 0x1, track);
}

void playDoorOpenSound(void)
{
    playTrack(1);
}

void playDoorCloseSound(void)
{
    playTrack(2);
}

void playSlopeOpenSound(void)
{
    playTrack(3);
}

void playSlopeCloseSound(void)
{
    playTrack(4);
}
