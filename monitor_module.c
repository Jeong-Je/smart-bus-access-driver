#define LCD_I2C_ADDR    ((0x27 << 1) & 0xFE)
#define LCD_BAUD_RATE   (100000)
#define LCD_BACKLIGHT   (0x08)
#define LCD_ENABLE      (0x04)
#define LCD_RS          (0x01)

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/

volatile uint8 monitorFlags;

static IfxI2c_I2c I2CModuleHandle;
static IfxI2c_I2c_Device monitorModuleHandle;

static uint8 customChar0[8] = {
        0x00, 0x1F, 0x1F, 0x1F, 0x1F, 0x1F, 0x1F, 0x00
};  // On 아이콘

static uint8 customChar1[8] = {
        0x00, 0x1F, 0x11, 0x11, 0x11, 0x11, 0x1F, 0x00
};  // Off 아이콘

